double MapProgressBarSizes(ProgressBarSizes size) {
  if (size == ProgressBarSizes.small) {
    return 8.0;
  } else {
    return 16.0;
  }
}

double MapProgressBarStylesBorderRadius(ProgressBarStyles style) {
  if (style == ProgressBarStyles.sharp) {
    return 0.0;
  } else if (style == ProgressBarStyles.soft) {
    return 4.0;
  } else {
    return 100.0;
  }
}

